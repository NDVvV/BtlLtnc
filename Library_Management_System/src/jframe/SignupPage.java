    //insert values into users table
    public void insertSignupDetails() {
        String name = txt_username.getText();
        String pwd = txt_password.getText();
        String email = txt_email.getText();
        String contact = txt_contact.getText();
        
        try {
            Connection con = DBConnection.getConnection();
            String sql = "insert into users(user_name,password,email,contact) values (?,?,?,?) ";
            PreparedStatement pst = con.prepareStatement(sql);
            
            pst.setString(1, name);
            pst.setString(2, pwd);
            pst.setString(3, email);
            pst.setString(4, contact);
            
            int updateRowCount = pst.executeUpdate();
            
            if(updateRowCount > 0) {
                JOptionPane.showMessageDialog(this, "Register Successfully");
                LoginPage login = new LoginPage();
                login.setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Register Failure");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    
    //validate value input
    public boolean validateSignup() {
        String name = txt_username.getText();
        String pwd = txt_password.getText();
        String email = txt_email.getText();
        String contact = txt_contact.getText();
        String regex = "^[a-zA-Z0-9_!#$%&'*+/=?`{|}~^-]+(?:\\\\.[a-zA-Z0-9_!#$%&'*+/=?`{|}~^-]+â†µ\\n\" +\n" +
            "\")*@[a-zA-Z0-9-]+(?:\\\\.[a-zA-Z0-9-]+)*$";
        if(name.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter username!");
            return false;
        }
        if(pwd.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter password!");
            return false;
        }
        if(email.equals("")||email.matches(regex)) {
            JOptionPane.showMessageDialog(this, "Please enter valid email!");
            return false;
        }
        if(contact.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter contact number!");
            return false;
        }
        return true;
    }
    
    //check duplicate users
    public boolean checkDuplicateUser() {
        String name = txt_username.getText();
        boolean isExist = false;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_ms","root","");
            
            PreparedStatement pst = con.prepareStatement("select * from users where user_name = ?");
            pst.setString(1, name);
            
            ResultSet rs = pst.executeQuery();
            if(rs.next()) {
                isExist = true;
            }else{
                isExist = false;
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        return isExist;
    }
    
    
